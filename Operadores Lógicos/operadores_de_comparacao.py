# Operadores de comparação
'''
São operadores utilizados para comparar dois valores
'''
# Igualdade
'''
saldo = 450
saque = 200

print(saldo == saque)
>>> False
'''
# Diferença
'''
saldo = 450
saque = 200

print(saldo != saque)
>>> True
'''
# Maior que / maior ou igual
'''
saldo = 450
saque = 200

print(saldo > saque)
>>> True

print(saldo >= saque)
>>> True
'''
# Menor que / menor ou igual
'''
saldo = 450
saque = 200

print(saldo < saque)
>>> False

print(saldo <= saque)
>>> False
'''

saldo = 200
saque = 200

print(saldo == saque)
print(saldo != saque)
print(saldo > saque)
print(saldo >= saque)
print(saldo < saque)
print(saldo <= saque)
