# Tipos em Python (mais comuns)
'''
Texto = str
Numerico = int, float, complex
Sequencia (numerica 12345, abc, 1 23 45 -2, g u i l h e r m e) = list, tuple, range
Mapa (chave/valor = chave: nome // valor: guilherme) = dict
Coleção (parecido com LISTA, porem nao permite valores repetidos) = set, frozenset
Booleano (verdadeiro/falso) = bool
Binário = bytes, bytearray, memoryview
'''

# Tipos numericos
'''
Numeros Inteiros
1, 2, 10, 100...

Numeros de pontos flutuantes
1.5, -10.543, 0.76...

'''

# Tipo Booleano (true / false)
'''
Em python o tipo booleano é uma subclasse de 'int'
Qualquer numero diferente de 0 = verdadeiro (true)
0 = falso (false)

'''

# Strings
'''
Cadeia de caracteres sao usadas para representar valores alfanumericos.
Exemplos de string: "Python", 'Python', """Python""", "p"
'''
print(11 + 10 + 1000) #inteiro
print(1.5 + 1 + 0.5) #flutuante
print(True) #booleano
print(False) #booleano
print("Python") #string


