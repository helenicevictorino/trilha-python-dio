# Modo interativo
'''
O interpretador Python pode executar em modo que possibilite o desenvolvedor 
a escrever o código, e ver o resultado na hora 
'''

# Iniciando o modo interativo
'''
Usando o terminal: escrevendo python (para sair usar: exit())
Executando o script com a flag -i (python -i app.py)
'''

#Funções dir e help
'''
dir:
Sem argumentos, retorna a lista de nomes no escopo local atual.
Com um argumento, retorna uma lista de atributos validos para o objeto.
Exemplo:
dir()
dir(100)

help:
Invoca o sistema de ajuda integrado. É possivel fazer buscas em modo interativo ou informar
por parametro qual o nome do módulo, função, classe, método ou variavel. 
Exemplo:
help()
help(100)
'''
